package com.example.signuploginrealtime;

public class pavillion_customer {

    String pavillion_bname;
    String pavillion_bday;
    String pavillion_btime;
    String pavillion_bphone;

    public pavillion_customer() {
    }

    public pavillion_customer(String pavillion_bname, String pavillion_bday, String pavillion_btime, String pavillion_bphone) {
        this.pavillion_bname =
                pavillion_bname;
        this.pavillion_bday =
                pavillion_bday;
        this.pavillion_btime =
                pavillion_btime;
        this.pavillion_bphone =
                pavillion_bphone;
    }

    public String getPavillion_bname() {
        return pavillion_bname;
    }

    public void setPavillion_bname(String pavillion_bname) {
        this.pavillion_bname =
                pavillion_bname;
    }

    public String getPavillion_bday() {
        return pavillion_bday;
    }

    public void setPavillion_bday(String pavillion_bday) {
        this.pavillion_bday =
                pavillion_bday;
    }

    public String getPavillion_btime() {
        return pavillion_btime;
    }

    public void setPavillion_btime(String pavillion_btime) {
        this.pavillion_btime =
                pavillion_btime;
    }

    public String getPavillion_bphone() {
        return pavillion_bphone;
    }

    public void setPavillion_bphone(String pavillion_bphone) {
        this.pavillion_bphone =
                pavillion_bphone;
    }
}
